const (
	maxKnownTxs       = 32768 // Maximum transactions hashes to keep in the known list (prevent DOS)
	maxKnownProposals = 1024  // Maximum block hashes to keep in the known list (prevent DOS)
	handshakeTimeout  = 5 * time.Second
)

// PeerInfo represents a short summary of the KUSD sub-protocol metadata known
// about a connected peer.
type PeerInfo struct {
	Version int    `json:"version"` // Kowala protocol version negotiated
	Head    string `json:"head"`    // SHA3 hash of the peer's best owned block
}

