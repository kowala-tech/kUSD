// peerState contains the known state of a peer
type peerState struct {
	height *big.Int // block number
	round  uint64   // round
	startTime
	step                     RoundStepType       // Step peer is at
	StartTime                time.Time           // Estimated start of round 0 at this height
	hasProposal              bool                // True if peer has proposal for this round
	proposalBlockPartsHeader types.PartSetHeader //
	proposalBlockParts       *types.BitArray     //
	proposalPOLRound         uint64              // Proposal's POL round. -1 if none.
	proposalPOL              *types.BitArray     // nil until ProposalPOLMessage received.
	prevotes                 *types.BitArray     // All votes peer has for this round
	precommits               *types.BitArray     // All precommits peer has for this round
	lastCommitRound          int                 // Round of commit for last height. -1 if none.
	lastCommit               *types.BitArray     // All commit precommits of commit for last height.
	catchupCommitRound       int                 // Round that we have commit for. Not necessarily unique. -1 if none.
	catchupCommit            *types.BitArray     // All commit precommits peer has for this height & CatchupCommitRound
}

func newPeer(version int, p *p2p.Peer, rw p2p.MsgReadWriter) *peer {
	id := p.ID()

	return &peer{
		Peer:           p,
		rw:             rw,
		version:        version,
		id:             fmt.Sprintf("%x", id[:8]),
		knownTxs:       set.New(),
		knownProposals: set.New(),
		knownVotes:     set.New(),
	}
}

// Info gathers and returns a collection of metadata known about a peer.
func (p *peer) Info() *PeerInfo {
	hash := p.Head()

	return &PeerInfo{
		Version: p.version,
		Head:    hash.Hex(),
	}
}

// Head retrieves a copy of the current head hash of the peer.
func (p *peer) Head() (hash common.Hash) {
	p.lock.RLock()
	defer p.lock.RUnlock()

	copy(hash[:], p.head[:])
	return hash
}

// SetHead updates the head hash of the peer.
func (p *peer) SetHead(hash common.Hash) {
	p.lock.Lock()
	defer p.lock.Unlock()

	copy(p.head[:], hash[:])
}

